# Variable types in Python
print("*** Variable types in Python ***")

# 1. integers
number = 10
print("integers: ", number)

# 2. float
float_number = 10.0
print("float: ", float_number)

# 3. string
text = "Hello, World!"
print("string: ", text)

# 4. Boolean
is_active = True
print("Boolean: ", is_active)

# 5. list
list_data = [1, 2, 3, 4, 5]
print("list: ", list_data)

# 6. tuple
tuple_data = (1, 2, 3, 4, 5)
print("tuple: ", tuple_data)

# 7. dictionary
dict_data = {
    "name": "John",
    "age": 30
}
print("dictionary: ", dict_data)
