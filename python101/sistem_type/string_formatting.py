# String formatting in Python
print("*** String formatting in Python ***")

# 1. integers
count = 10
print(f"Today, I saw {count} dogs in the park")

# 2. float
cost = 10.99
print(f"The cost of the dinner was {cost} dollars")

# 3. string
greeting = "Hello, good morning!"
print(f"Message: {greeting}")

# 4. Boolean
is_true = True
print(f"The statement is {is_true}")

# 5. list
fruits = ["apples", "bananas", "oranges"]
print(f"Today I went to the market and bought {fruits}")

# 6. tuple
coordinates = (45.04, 7.42)
print(f"The treasure's coordinates are {coordinates}")

# 7. dictionary
user = {
    "name": "John",
    "age": 30
}
print(f"The user's name is {user['name']} and their age is {user['age']}")
